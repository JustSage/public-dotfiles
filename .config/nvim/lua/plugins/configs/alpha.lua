local function footer()
	local total_plugins = #vim.tbl_keys(packer_plugins)
	return "   Have Fun with neovim"
		.. "   v"
		.. vim.version().major
		.. "."
		.. vim.version().minor
		.. "."
		.. vim.version().patch
		.. "   "
		.. total_plugins
		.. " plugins"
end

